let trafficlight=prompt("enter the color");

switch(trafficlight){
    case("red"):
    console.log("Stop Immediately...");
    break;
    case("yellow"):
    console.log("Prepare to stop...");
    break;
    case("green"):
    console.log("Proceed or continue driving...");
    break;
    default:
        console.log("Invalid traffic light color...");
 
    
}